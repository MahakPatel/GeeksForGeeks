class Solution {
  public:
    void deleteAllOccurOfX(struct Node** head_ref, int x) {
        Node *temp = *head_ref;
        
        while(temp != NULL){ // check untill last node
            if(temp -> data == x){ // if LL data is same then the given key 
                if(temp == *head_ref){ // if head value is same as given key
                     *head_ref = temp -> next; // second node become new head
                }
                Node *nextNode = temp-> next; // nextnode will be the temo of next
                Node *prevNode = temp-> prev; // previous node is aleays temp-> prev
                 
                if(nextNode) nextNode-> prev = prevNode; // if it have next node (is condition is not for lst node)
                if(prevNode) prevNode-> next = nextNode; // if it have previous node (is condition is not for head node)
                
                free(temp);
                temp = nextNode; // move temp to nextNode
            }else{
                temp = temp -> next; // if value is not same then move temp to next noide
            }
            
        }
        
        return;
    }
};

